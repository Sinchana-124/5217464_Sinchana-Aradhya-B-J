def maxSubarray(arr):
    # Write your code here
    sub = arr[0]
    cur = arr[0]

    for x in arr[1:]:
        cur = max(x, cur + x)
        sub = max(sub, cur)

    seq = max(arr)
    if seq > 0:
        seq = sum(i for i in arr if i > 0)

    return [sub, seq]
    

